# find.py
"""
.find() takes a string as an argument and
searching the string it was run on for that
string. It then returns the first index
value where that string is located.
"""

god_wills_it_line_one = "The very earth will disown you"

disown_placement = god_wills_it_line_one.find("disown")
print("disown was found at index:",disown_placement)
